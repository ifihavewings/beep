  String _get(String key) {
    final localeKey = localeKeyFromLocale(locale);
    return _localizedStrings[localeKey]?[key] ??
        _localizedStrings[locale.languageCode]?[key] ??
        _localizedStrings['en']?[key] ??
        key;
  }

  /// Replace placeholders in string
  String _format(String key, Map<String, String> params) {
    String result = _get(key);
    params.forEach((k, v) {
      result = result.replaceAll('{$k}', v);
    });
    return result;
  }

