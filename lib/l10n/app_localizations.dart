  /// Determine locale key with script/country support
  static String localeKeyFromLocale(Locale locale) {
    if (locale.scriptCode != null && locale.scriptCode!.isNotEmpty) {
      return '${locale.languageCode}-${locale.scriptCode}';
    }
    if (locale.countryCode != null && locale.countryCode!.isNotEmpty) {
      return '${locale.languageCode}-${locale.countryCode}';
    }
    return locale.languageCode;
  }

  /// Check if a locale is supported
  static bool isSupportedLocale(Locale locale) {
    final localeKey = localeKeyFromLocale(locale);
    return _localizedStrings.containsKey(localeKey) ||
        _localizedStrings.containsKey(locale.languageCode);
  }

